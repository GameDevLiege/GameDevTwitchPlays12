using System.Collections;
using System.Collections.Generic;
using UnityEditor;
using UnityEngine;
[CustomEditor(typeof(AutoConnectTwitchIRC))]
public class AutoConnectTwitchIRCEditor : Editor{

	
    public override void OnInspectorGUI()
    {
        DrawDefaultInspector();
        AutoConnectTwitchIRC myScript = (AutoConnectTwitchIRC)target;
        if (GUILayout.Button("Get your Oauth token"))
        {
            AutoConnectTwitchIRC.GetOAuthFromWeb("http://www.twitchapps.com/tmi/");
        }
    }
}
